let currentLang = 'en';

document.addEventListener('DOMContentLoaded', () => {

    // --- Language Toggle Logic ---
    const langBtn = document.getElementById('lang-toggle');
    const langText = langBtn.querySelector('.lang-text');
    const elementsToTranslate = document.querySelectorAll('[data-i18n]');
    
    // Check local storage for language preference
    const savedLang = localStorage.getItem('ehhs_language');
    if (savedLang && ['en', 'es'].includes(savedLang)) {
        currentLang = savedLang;
        updateLanguage(currentLang);
    }

    langBtn.addEventListener('click', () => {
        currentLang = currentLang === 'en' ? 'es' : 'en';
        localStorage.setItem('ehhs_language', currentLang);
        updateLanguage(currentLang);
    });

    function updateLanguage(lang) {
        // Change button text
        langText.textContent = translations[lang]['toggle_lang'];
        langBtn.innerHTML = lang === 'en' ? '<span class="lang-text">EN</span> | <span>ES</span>' : '<span>EN</span> | <span class="lang-text">ES</span>';
        
        // Update document lang attribute
        document.documentElement.lang = lang;
        
        // Update all translated elements
        elementsToTranslate.forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[lang][key]) {
                // If it's an input placeholder or option, update text appropriately
                if(element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    if(element.placeholder) element.placeholder = translations[lang][key];
                } else {
                    element.innerHTML = translations[lang][key]; // innerHTML allows bold tags if needed
                }
            }
        });
    }

    // --- Current Year in Footer ---
    const yearSpan = document.getElementById('currentYear');
    if(yearSpan) {
        yearSpan.textContent = new Date().getFullYear();
    }

    // --- Mobile Navigation Toggle ---
    const navToggle = document.querySelector('.mobile-nav-toggle');
    const primaryNav = document.getElementById('primary-navigation');
    const navLinks = document.querySelectorAll('.nav-link');

    navToggle.addEventListener('click', () => {
        const isVisible = primaryNav.getAttribute('data-visible') === 'true';
        if (isVisible) {
            primaryNav.setAttribute('data-visible', false);
            navToggle.setAttribute('aria-expanded', false);
        } else {
            primaryNav.setAttribute('data-visible', true);
            navToggle.setAttribute('aria-expanded', true);
        }
    });

    // Close mobile nav when clicking a link
    navLinks.forEach(link => {
        link.addEventListener('click', () => {
            primaryNav.setAttribute('data-visible', false);
            navToggle.setAttribute('aria-expanded', false);
        });
    });

    // --- Header Scroll Effect ---
    const header = document.querySelector('.header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.style.boxShadow = '0 4px 20px rgba(0,0,0,0.08)';
            header.style.padding = '0.5rem 0';
        } else {
            header.style.boxShadow = '0 2px 10px rgba(0,0,0,0.05)';
            header.style.padding = '1rem 0';
        }
    });

    // --- Scroll Reveal Animations ---
    const reveals = document.querySelectorAll('.reveal-up, .reveal-left, .reveal-right');

    const revealOptions = {
        threshold: 0.15,
        rootMargin: "0px 0px -50px 0px"
    };

    const revealOnScroll = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (!entry.isIntersecting) {
                return;
            } else {
                entry.target.classList.add('active');
                observer.unobserve(entry.target);
            }
        });
    }, revealOptions);

    reveals.forEach(reveal => {
        revealOnScroll.observe(reveal);
    });

    // Trigger immediately for elements in view on load
    setTimeout(() => {
        reveals.forEach(reveal => {
            const rect = reveal.getBoundingClientRect();
            if(rect.top < window.innerHeight) {
                reveal.classList.add('active');
            }
        });
    }, 100);

    // --- Form Submission ---
    const contactForm = document.getElementById('contactForm');
    if(contactForm) {
        contactForm.addEventListener('submit', (e) => {
            e.preventDefault();
            const btn = contactForm.querySelector('button[type="submit"]');
            const originalText = btn.textContent;
            
            btn.textContent = currentLang === 'en' ? "Sending..." : "Enviando...";
            btn.disabled = true;

            // Simulate sending
            setTimeout(() => {
                contactForm.reset();
                btn.textContent = currentLang === 'en' ? "Message Sent Successfully!" : "¡Mensaje enviado con éxito!";
                btn.style.backgroundColor = 'var(--color-sage)';
                btn.style.color = '#fff';
                
                setTimeout(() => {
                    btn.textContent = originalText;
                    btn.disabled = false;
                    btn.style.backgroundColor = '';
                    btn.style.color = '';
                }, 4000);
            }, 1500);
        });
    }
});
